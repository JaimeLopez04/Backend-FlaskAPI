from flask import Flask, jsonify, request
from flask_pymongo import PyMongo,ObjectId
from config import db


def login():
    users = []
    for doc in db.find():
        users.append({
            '_id': str(ObjectId(doc['_id'])),
            'name': doc['name'],
            'email': doc['email'],
            'password': doc['password']
        })
    return jsonify(users)